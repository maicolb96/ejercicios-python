"""
Suponga que un individuo desea invertir su capital 
en un banco y desea saber cuánto dinero ganara 
después de un mes si el banco paga a razón de 2% mensual.
"""

print("Este algoritmo indica cuanto gana una persona al invertir en un banco al mes.\n")

inversion=float(input("Ingrese el monto a invertir: "))

print(f"Como la tasa de interés mensual es de 2% usted ganaría al mes ${inversion*0.02} adicional a su inversión.")